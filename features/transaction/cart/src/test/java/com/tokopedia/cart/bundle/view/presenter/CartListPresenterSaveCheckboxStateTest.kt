        beforeEachTest {
            cartListPresenter.attachView(view)
        }

        Scenario("success save checkbox state") {

            val cartItemDataList = ArrayList<CartItemHolderData>().apply {
                add(
                        CartItemHolderData(
                                isSelected = true,
                                cartId = "123"
                        )
                )
