package com.tokopedia.linker.model;

public class LinkerShareResult {

    private String shareContents;
    private String shareUri;
    private String url;

    public String getShareContents() {
        return shareContents;
    }

    public void setShareContents(String shareContents) {
        this.shareContents = shareContents;
    }

    public String getShareUri() {
        return shareUri;
    }

    public void setShareUri(String shareUri) {
        this.shareUri = shareUri;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

}
